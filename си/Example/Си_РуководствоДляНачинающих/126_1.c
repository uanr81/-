#include <stdio.h>
#include <string.h>

int main()
{
  printf(" деление целых: 5/4 это %d \n", 5 / 4);
  printf(" деление целых 6/3 это %d \n", 6 / 3);
  printf(" деление целых 7/4 это %d \n", 7 / 4);
  printf(" деление чисел с плавающей точкой 7.0 /4.0 это %2.2f \n", 7.0 / 4.0 );
  printf(" смешанное деление 7.0/4 это %2.2f \n", 7.0 / 4);
}
